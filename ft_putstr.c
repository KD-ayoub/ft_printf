#include "ft_printf.h"

int	ft_putstr(char	*p)
{
	int	l;

	l = 0;
	if (!p)
	{
		write (1, "(null)", 6);
		return (6);
	}
	while (*p)
		l += ft_putchar(*p++);
	return (l);
}
